                                        Desa Tugumukti <b>“BERMANFAAT”</b></p>
                                       <span align="center"> <p>(<b>B</b>ersih,<b>E</b>tika/Elegan,<b>R</b>asional,<b>M</b>aju,<b>A</b>ccountable,<b>N</b>asionalis,<b>F</b>ungsional,<b>A</b>gamis,<b>A</b>man,<b>T</b>ertib)</p></span>

                    <p>1.   <b>B</b>ersih</p>
                    <p>a.  Meningkatkan kebersihan dalam pola pembangunan sarana prasarana, diantaranya :</p>
                        <p>-Menyediakan tempa </p>
                        <p>-tempat smapah diberbagai tempat yang dianggap layak dan tidak menggangu prasarana umum</p>
                    <p>b.  Akan selalu mengkoordinasikan dengan masyarakat untuk selalu menjaga kebersihan baik dilingkungan sendiri maupun dijalan – jalan umum denagn cara menertibkan saluran air yang dapat mengakibatkan banjir dan untuki menjaga keutuhan jalan yang telah dibangun  dan selalu mengkoordinasikan dengan masyarakat dengan Pola PHBS (Perilaku Hidup Bersih dan Sehat)</p>
                    <p>2.  <b>E</b>tika (Elegan)</p>
                    <p>a.  Supel dalam menampung aspirasi masyarakat secara elegan (etika) dalam artian sopan ,santun, dalam kehidupan sehari hari dimasyarakat dalam mengayomi dan melindungi</p>
                    <p>b.  Pemerintah Desa akan membawa secara elegan dalam artian sopan santun, saling hormat menghormati tidak ada batasan antara pemerintah dan masyarakat sehingga masing – masing mempunyai rasa tanggung jawab dan kewajiban untuk memajukan kesejahteraan dimasyarakat.</p>
                    <p>3.  <b>R</b>asional</p>
                    <p>a.  Rasional dalam berfikir untuk meningkatkan kualitas kehidupan dimasyarakat kepada sesuatu keterampilan dengan mengikutsertakan pelatihan – pelatihan sesuai dengan program yang ada, baik ditingkat Desa mauppun di Kabupaten , Provinsi ataupun Pusat, sehingga masyarakat kita mempunyai keahlian masing – masing dan tidak mudah untuk dibohongi dibawa ke hal yang tidak masuk akal contohnya kepada hal – hal mistik, penggandaan uang, pencari kerja dengan iming – iming gaji yang besar, sehingga menimbulkan Traficking dll</p>
                    <p>b.  Pemerintah Desa akan membawa dan memotivasi kepada segenap masyarakat untuk menggapai cita – cita kedalam kehidupan yang lebih baik</p>
                    <p>c.  Meningkatkan pendidikan baik pendidikan Formal maupun Non formal mungkin dengan cara tersebut masyarakat Tugumukti akan lebih meningkat  dalam SDM (Sumber Daya Manusia)</p>
                    <p>d.  Meningkatkan keimanan dan ketaqwaan terhadap Alloh Swt</p>
                    <p>e.  Meningkatkan keimanan dimasing – masing wilayah dengan cara ronda malam dan pengontrolan dari pihak Desa / pihak terkait sehingga tumbuh sinergitas antara masyarakat dengan Pemerintahan Desa di masing – masing wilayah</p>
                    <p>f.    Pemerintahan Desa dan Masyarakat akan selalu menjunjung tinggi sifat gotong royong sabilulungan dan sauyunan sehingga Desa Tugumukti kedepan menjadikan Desa yang “Baldatun Toyiban Warobbun Gofur”, Desa yang Gemah Ripah Loh Jinawi Kerta Raharja sebagai wujud Visi Misi Bandung Barat CERMAT</p>
                    <p>4.  <b>M</b>aju</p>
                            <p>Dengan berfikir untuk menciptakan kwalitas masyarakat yang profesional dan proporsional dengan ditunjang oleh SDM dari hasil pelatihan maupun pendidikan lainnya, maka  masyarakat Desa Tugumukti akan lebih maju sesuai harapan masyarakat</p>
                    <p>5.  <b>A</b>ccountable (Bertanggungjawab)</p>
                            <p>Bertanggung jawab sepenuhnya dalam hal pengeloaan dalam proses kerja pemerintahan Desa Tugumukti dalam melaksanakan dan menjalankan Program 
                            – program kedepan sesuai rencana kerja demi untuk mensejahterakan masyarakat</p>
                    <p>6.  <b>N</b>asionalis
                            <p>Dalam pelaksanaan tata kelola masyarakat wilayah Desa Tugumukti akan didasari  dengan jiwa dan sifat kepahlawanan, dalam arti tanpa pamrih dengan mengedepankan dan mengutamakan kepentingan umum dari pada kepentingan pribadi, golongan / individu dan kepentingan – kepentingan keluarga.</p>

                    <p>7.  <b>F</b>ungsional
                            <p>Untuk  mewujudkan roda pemerintahan Desa Tugumukti yang kondusif dan tatakelola yang teratur, profesional dan proporsional akan menerapkan Tugas Pokok dan Fungsi baik Aparatur Desa maupun Lembaga Desa.</p>
                    <p>8.  <b>A</b>gamis</p>
                    <p>Untuk meningkatkan hidup beragama </p>
                   <p> a.  Semakin menggiatka pengajian – pengajian di tiap Dusun, Madrasah dan sarana lainnya</p>
                   <p> b.  Mengembangkan sarana prasarana yang menunjang untuk kegiatan keagamaan</p>
                   <p> c.  Berkoordinasi dengan para alim ulama untuk meningkatkan kehidupan beragama</p>
                   <p> d.  Berkoordinasi dengan ulama dan dinas terkait untuk peningkatan pendidikan agama secara formal atau non formal </p>
                   <p> 9.  <b>A</b>man</p>
                            <p>Memberikan keamanan dan kenyamanan Pemerintahan dan lingkungan masyarakat Desa Tugumukti dengan memfungsikan Aparatur Desa dan Lembaga untuk menumbuhkembangkan kearifan lokal</p>
                    <p>10. <b>T</b>ertib</p>
                            <p>Menertibkan Tugas Pokok  dan Fungsi aparatur Pemerintahan Desa serta  Lembaga bersama masyarakat Desa Tugumukti sampai meyakini bahwa Tertib itu  indah.</p>

